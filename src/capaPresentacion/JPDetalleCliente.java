        public void listarTabla() {
        cl = conec.ListaCliente();
        DefaultTableModel modelo = (DefaultTableModel) tablaCliente.getModel();
        for (Cliente e : cl) {
            modelo.addRow(new Object[]{e.getId(), e.getNombres_apellidos(), e.getCorreoelectronico(), e.getDireccion(), e.getTelefono(), e.getCedula(), e.getFecha_registro()});
        }
    }

