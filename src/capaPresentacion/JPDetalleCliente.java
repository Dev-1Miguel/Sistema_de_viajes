    public void limpiarDatos() {
        DefaultTableModel modelo = (DefaultTableModel) tablaCliente.getModel();
        for (cont = tablaCliente.getRowCount() - 1; cont >= 0; cont--) {
            modelo.removeRow(cont);
        }
    }

