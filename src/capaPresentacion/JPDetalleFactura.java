    public void limpiarDatos() {
        DefaultTableModel modelo = (DefaultTableModel) tablaFactura.getModel();
        for (cont = tablaFactura.getRowCount() - 1; cont >= 0; cont--) {
            modelo.removeRow(cont);
        }
    }

