    public void listarTabla() {
        cl = conec.ListaFactura();
        DefaultTableModel modelo = (DefaultTableModel) tablaFactura.getModel();
        for (Factura e : cl) {
            modelo.addRow(new Object[]{e.getId(), e.getReserva_id(), e.getFecha_emision(), e.getMetodo(), e.getSubTotal(), e.getIva(), e.getTotal(), e.getEstado()});
        }
    }

